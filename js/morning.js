// Elements for slider and progress
const adhkarListDiv = document.getElementById('morning-adhkar-slider');
const progressText = document.getElementById('adhkar-progress-text');
const progressFill = document.getElementById('adhkar-progress-fill');
const prevBtn = document.getElementById('adhkar-prev-btn');
const nextBtn = document.getElementById('adhkar-next-btn');
let currentAdhkar = 0;

// Render current adhkar text and update progress
function renderMorningAdhkarSlider() {
  if (!adhkarListDiv) return;
  // Show current adhkar text
  adhkarListDiv.innerHTML = `<div class="morning-adhkar-item">${morningAdhkar[currentAdhkar]}</div>`;
  // Update progress bar and text
  if (progressText) {
    progressText.textContent = `${currentAdhkar + 1} / ${morningAdhkar.length}`;
    progressText.style.display = '';
  }
  if (progressFill) {
    const percent = ((currentAdhkar + 1) / morningAdhkar.length) * 100;
    progressFill.style.width = percent.toFixed(0) + '%';
    progressFill.style.display = '';
  }
  // Hide navigation if at ends
  if (prevBtn) prevBtn.disabled = (currentAdhkar === 0);
  if (nextBtn) nextBtn.disabled = (currentAdhkar === morningAdhkar.length - 1);
}

// Attach event listeners for navigation buttons after DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
  renderMorningAdhkarSlider();
  if (prevBtn) {
    prevBtn.onclick = () => {
      if (currentAdhkar > 0) {
        currentAdhkar--;
        renderMorningAdhkarSlider();
      }
    };
  }
  if (nextBtn) {
    nextBtn.onclick = () => {
      if (currentAdhkar < morningAdhkar.length - 1) {
        currentAdhkar++;
        renderMorningAdhkarSlider();
      }
    };
  }
  // Optionally, could add swipe gesture support for mobile
});
